max = int(input('Введите максимум диапазона: '))

list_result = list()
count = 0

for i in range(len(array)):    
    if array[i] > min and array[i] < max:
        list_result.append(i)
        

print(f"Индексы элементов списка, находящихся в диапазоне от {min} до {max}: {list_result}")


