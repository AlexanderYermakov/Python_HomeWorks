# Задача 30:  Заполните массив элементами арифметической прогрессии. 
# Её первый элемент, разность и количество элементов нужно ввести с клавиатуры. 
# Формула для получения n-го члена прогрессии: an = a1 + (n-1) * d.
# Каждое число вводится с новой строки.

first_num = int(input('Введите первый элемент прогрессии:\n '))
difference = int(input('Введите разность элементов:\n '))
quantity = int(input('Введите количество элементов прогрессии:\n '))

result = [i for i in range(first_num, (first_num + (quantity - 1) * difference) + 1, difference)]

print(result)


